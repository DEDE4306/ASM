STKSEG SEGMENT STACK
DW 32 DUP(0)
STKSEG ENDS

DATASEG SEGMENT
    LETTER DB "A"
DATASEG ENDS

CODESEG SEGMENT
    ASSUME CS:CODESEG, DS:DATASEG

MAIN PROC FAR
    MOV AX, DATASEG
    MOV DS, AX

    MOV CX, 2
    MOV AH, 02H

OUTER_LOOP:
    MOV BX, CX
    MOV CX, 13    

INNER_LOOP:
    
    MOV AL, LETTER
    MOV DL, AL
    INC AL
    MOV LETTER, AL
    INT 21H

    LOOP INNER_LOOP

    MOV DL, 0DH            ; 回车
    INT 21H
    MOV DL, 0AH            ; 换行
    INT 21H

    MOV CX, BX
    LOOP OUTER_LOOP

    MOV AX, 4C00H
    INT 21H

MAIN ENDP
CODESEG ENDS

    END MAIN



